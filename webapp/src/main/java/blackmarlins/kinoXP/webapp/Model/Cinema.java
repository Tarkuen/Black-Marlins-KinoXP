package blackmarlins.kinoXP.webapp.Model;

public class Cinema {

    Integer cinema_ID;
    Integer cinema_seats;

    public Integer getCinema_ID() {
        return cinema_ID;
    }

    public void setCinema_ID(Integer cinema_ID) {
        this.cinema_ID = cinema_ID;
    }

    public Integer getCinema_seats() {
        return cinema_seats;
    }

    public void setCinema_seats(Integer cinema_seats) {
        this.cinema_seats = cinema_seats;
    }
}
